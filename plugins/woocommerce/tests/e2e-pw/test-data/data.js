function getTranslationFor(textToTranslate) {
	let langCode = 'en-US';

	if ( LANGUAGE ) {
		if ( [ 'en-US', 'es-ES' ].includes( LANGUAGE ) ) {
			langCode = LANGUAGE;
		} else {
			console.log( 'LANGUAGE input must be en-US or es-ES' );
		}
	}

	return languageObject[ langCode ][ textToTranslate ];
}

